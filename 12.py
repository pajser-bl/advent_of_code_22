def bfs(_map, start):
    queue = collections.deque([[start]])
    seen = set([start])
    while queue:
        path = queue.popleft()
        cx, cy, cc = path[-1]
        if cc == 'E':
            return path
        if cc == 'S':
            cc = 'a'
        for x, y in ((cx + 1, cy), (cx - 1, cy), (cx, cy + 1), (cx, cy - 1)):
            if 0 <= x < x_len and 0 <= y < y_len and _map[y][x] not in seen and ord(cc) + 1 >= ord(
                    _map[y][x][2] if _map[y][x][2] != 'E' else 'z'):
                queue.append(path + [_map[y][x]])
                seen.add((_map[y][x]))


